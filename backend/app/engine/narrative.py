"""
叙事框架识别引擎 — 检测信息中嵌入的操纵性叙事模式

叙事框架是"信息的壳"——同一个事实被套入不同的叙事框架后，
会产生完全不同的解读和影响。

8 种常见的操纵性叙事框架:
1. 阴谋论 — "他们暗中操控一切"
2. 对立叙事 — "我们 vs 他们" (us vs them)
3. 受害者民族主义 — 利用被压迫叙事获取道德正当性
4. 恐惧营销 — 制造恐慌以推动传播
5. 辉煌过去 — "以前多好，现在多糟" (golden age fallacy)
6. 伪科学包装 — 用科学术语包装谬论
7. Whataboutism — 转移焦点到另一事件
8. 妖魔化 — 将对手描绘为邪恶势力

检测原理:
每种叙事框架有其标志性的语言模式、修辞策略和论证结构。
引擎通过模式匹配+上下文分析识别这些框架。
"""

