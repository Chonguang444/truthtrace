# 主检测函数
# =============================================================================

def detect_narratives(text: str, title: str = "") -> NarrativeAnalysis:
    """
    对输入文本进行 8 种操纵性叙事框架的全面检测。

    注意: 检测到叙事框架不等于信息本身虚假。
    叙事框架是关于'信息如何被呈现和运作'的分析，而非事实判断。
    """
    combined = f"{title}\n{title}\n{text}" if title else text

    all_matches: list[NarrativeMatch] = []

    for narrative_type, patterns in ALL_NARRATIVE_PATTERNS:
        for pattern_regex, description in patterns:
            for match in re.finditer(pattern_regex, combined, re.IGNORECASE):
                snippet = _extract_context(combined, match.start(), match.end())
                markers = _extract_markers(match.group())

                if not _is_duplicate(all_matches, narrative_type, snippet):
                    all_matches.append(NarrativeMatch(
                        narrative_type=narrative_type,
                        description=description,
                        confidence=_narrative_confidence(narrative_type, combined),
                        markers=markers,
                        reasoning=f"检测到叙事标记: {', '.join(markers[:3])} → {NARRATIVE_DESCRIPTIONS.get(narrative_type, '')[:100]}",
                    ))

    # 汇总
    if not all_matches:
        return NarrativeAnalysis(
            matches=[],
            dominant_narrative=None,
            manipulation_score=0.0,
            summary="未检测到明显的操纵性叙事框架。信息呈现方式相对中性。",
        )

    # 计算主导叙事
    type_count: dict[NarrativeType, int] = {}
    for m in all_matches:
        type_count[m.narrative_type] = type_count.get(m.narrative_type, 0) + 1

    dominant = max(type_count, key=type_count.get) if type_count else None

    # 操纵性评分: 0-100
    # 基础分: 每类叙事 +15, 高置信度匹配额外 +10
    manipulation_score = min(100.0, len(type_count) * 15.0 + sum(
        10.0 for m in all_matches if m.confidence in (Confidence.CERTAIN, Confidence.HIGH)
    ))

    # 汇总文本
    summary_parts = []
    for nt, cnt in sorted(type_count.items(), key=lambda x: -x[1]):
        label = _narrative_label(nt)
        summary_parts.append(f"{label} ({cnt}处)")

    summary = "检测到以下叙事框架: " + "; ".join(summary_parts) + \
              f"。操纵性评分: {manipulation_score:.0f}/100。" + \
              "请根据上下文判断这些叙事框架是否被用于操纵而非正当表达。"

    return NarrativeAnalysis(
        matches=all_matches,
        dominant_narrative=dominant,
        manipulation_score=manipulation_score,
        summary=summary,
    )


