def _extract_context(text: str, start: int, end: int, margin: int = 60) -> str:
    s = max(0, start - margin)
    e = min(len(text), end + margin)
    snippet = text[s:e].replace("\n", " ").strip()
    if s > 0:
        snippet = "…" + snippet
    if e < len(text):
        snippet += "…"
    return snippet


def _extract_markers(matched_text: str) -> list[str]:
    """提取触发的关键标记词"""
    markers = []
    # 提取引号内的关键词
    for m in re.finditer(r'[「「]([^」」]{2,15})[」」]', matched_text):
        markers.append(m.group(1))
    # 取显著词汇
    words = matched_text.strip().split()
    for w in words[:5]:
        if len(w) >= 2:
            markers.append(w)
    return markers[:6]


def _is_duplicate(existing: list[NarrativeMatch], new_type: NarrativeType, snippet: str) -> bool:
    for m in existing:
        if m.narrative_type == new_type:
            # Check if any marker or the description is similar
            if any(marker in snippet for marker in m.markers):
                return True
    return False


def _narrative_confidence(nt: NarrativeType, text: str) -> Confidence:
    """根据叙事类型和文本强度估算置信度"""
    # 强信号模式: 明显的妖魔化词汇
    if nt == NarrativeType.DEMONIZATION:
        strong = ["丧心病狂", "禽兽不如", "吃人血", "亡国灭种"]
        if any(kw in text for kw in strong):
            return Confidence.HIGH
        return Confidence.MODERATE

    # 强信号: 明确的阴谋论叙事
    if nt == NarrativeType.CONSPIRACY_THEORY:
        if re.search(r'(?:深层|影子|幕后).{0,5}(?:政府|势力)', text):
            return Confidence.HIGH
        return Confidence.MODERATE

    # 强信号: 酸碱体质等伪科学
    if nt == NarrativeType.SCIENTISM_ABUSE:
        if re.search(r'酸碱体质|碱性食物|酸性体质|排毒养颜|量子.{0,5}治疗', text):
            return Confidence.HIGH
        return Confidence.MODERATE

    return Confidence.MODERATE


